/**
 * 根据用户输入在知识库中检索相关条目
 * @param input 用户输入文本
 * @returns 匹配的知识条目列表（按相关度排序）
 */
export function searchKnowledge(input: string): HealthKnowledge[] {
  const lower = input.toLowerCase();
  const scored = healthKnowledge
    .map((item) => {
      let score = 0;
      for (const kw of item.keywords) {
        if (lower.includes(kw.toLowerCase())) {
          score += kw.length; // 更长的关键词匹配得分更高
        }
      }
      return { item, score };
    })
    .filter(({ score }) => score > 0)
    .sort((a, b) => b.score - a.score);

  return scored.map(({ item }) => item);
}

/**
 * 按分类获取知识条目
 */
export function getKnowledgeByCategory(
  category: HealthKnowledge["category"]
): HealthKnowledge[] {
  return healthKnowledge.filter((item) => item.category === category);
}
